

package com.nouhoun.springboot.jwt.integration.bean;

public class MessageBean {

	private String val;
	private String msg;

	public String getVal() {
		return val;
	}

	public void setVal(String val) {
		this.val = val;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
